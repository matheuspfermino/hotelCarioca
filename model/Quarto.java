public class Quarto {
    private String ID;
    private String NumPessoa;
    private String Tipo;

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getNumPessoa() {
        return NumPessoa;
    }

    public void setNumPessoa(String NumPessoa) {
        this.NumPessoa = NumPessoa;
    }

    public String getTipo() {
        return Tipo;
    }

    public void setTipo(String Tipo) {
        this.Tipo = Tipo;
    }
           
}
